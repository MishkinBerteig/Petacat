"""Integration tests for the help topics single-source-of-truth system.

These tests enforce that:

1. Every help topic file is valid JSON and conforms to the expected schema.
2. Every `componentName` referenced by `PanelHelpButton` in the frontend maps
   to an existing topic_key in the (English) JSON.
3. Every `topic_key` in the generated TypeScript constants file matches the
   JSON (i.e. the generation script hasn't drifted).
4. Every topic has required fields (topic_type, topic_key, title).

The single source of truth for all help text is `seed_data/help_topics.{locale}.json`.
The backend syncs it into the DB on every startup (upsert by topic_key), the
frontend fetches from `GET /api/docs/components/{key}`, and derived artifacts
(`HELP.md`, `client/src/constants/helpTopics.ts`) are generated by
`scripts/generate_help_docs.py`.
"""
from __future__ import annotations

import json
import re
from pathlib import Path

import pytest


REPO_ROOT = Path(__file__).resolve().parent.parent.parent
SEED_DIR = REPO_ROOT / "seed_data"
CLIENT_SRC = REPO_ROOT / "client" / "src"
TS_CONSTANTS = CLIENT_SRC / "constants" / "helpTopics.ts"


def _load_topics(locale: str = "en") -> list[dict]:
    """Load the help topics file for the given locale."""
    path = SEED_DIR / f"help_topics.{locale}.json"
    if not path.exists():
        # Legacy fallback
        path = SEED_DIR / "help_topics.json"
    with path.open() as f:
        return json.load(f)


@pytest.fixture
def topics_en() -> list[dict]:
    return _load_topics("en")


# ---------------------------------------------------------------------------
# Schema validation
# ---------------------------------------------------------------------------

def test_help_topics_file_is_valid_json():
    """The JSON source of truth must be parseable."""
    topics = _load_topics("en")
    assert isinstance(topics, list)
    assert len(topics) > 0, "help_topics.en.json is empty"


def test_every_topic_has_required_fields(topics_en):
    """Every topic entry must have topic_type, topic_key, and title."""
    for t in topics_en:
        assert "topic_type" in t, f"missing topic_type in {t}"
        assert "topic_key" in t, f"missing topic_key in {t}"
        assert "title" in t, f"missing title in {t}"
        assert t["topic_type"] in ("component", "glossary"), (
            f"invalid topic_type '{t['topic_type']}' for {t['topic_key']}"
        )
        assert t["topic_key"], f"empty topic_key for topic: {t}"
        assert t["title"], f"empty title for topic_key: {t['topic_key']}"


def test_topic_keys_are_unique(topics_en):
    """Each topic_key must appear at most once."""
    keys = [t["topic_key"] for t in topics_en]
    duplicates = {k for k in keys if keys.count(k) > 1}
    assert not duplicates, f"duplicate topic_keys: {duplicates}"


def test_topic_keys_use_snake_case(topics_en):
    """Topic keys should use lowercase snake_case for consistent URL mapping."""
    snake_case = re.compile(r"^[a-z][a-z0-9_]*$")
    for t in topics_en:
        assert snake_case.match(t["topic_key"]), (
            f"topic_key '{t['topic_key']}' is not snake_case"
        )


