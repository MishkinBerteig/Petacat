REQUIRED_COMPONENT_KEYS = {
    "problem_input",
    "run_controls",
    "workspace",
    "slipnet",
    "coderack",
    "temperature",
    "themespace",
    "trace",
    "memory",
    "commentary",
    "run_history",
    "admin",
}

# Admin action topics — each describes a button in the Admin panel. Their
# detailed user/technical descriptions live in metadata (not full_desc) so
# the AdminPanel can render them as structured bullet lists.
REQUIRED_ADMIN_ACTION_KEYS = {
    "admin_clear_memory",
    "admin_full_reset",
    "admin_regenerate_help",
}

VALID_BUTTON_VARIANTS = {"warning", "danger", "utility"}


def test_all_required_components_present(topics_en):
    """Every panel in the dashboard must have a corresponding help topic."""
    component_keys = {
        t["topic_key"] for t in topics_en if t["topic_type"] == "component"
    }
    missing = REQUIRED_COMPONENT_KEYS - component_keys
    assert not missing, (
        f"Required component help topics are missing: {sorted(missing)}"
    )


