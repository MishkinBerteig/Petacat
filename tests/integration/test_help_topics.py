# Frontend reference validation
# ---------------------------------------------------------------------------

def _collect_panel_help_button_refs() -> set[str]:
    """Find every `componentName="..."` usage of PanelHelpButton in the frontend.

    Returns the set of topic_keys referenced. Uses a simple regex scan since we
    don't want to parse TSX. If the codebase ever uses dynamic componentName
    (e.g. `componentName={variable}`), this scanner will miss those — but that
    is itself a convention we want to discourage in favour of static literals.
    """
    refs: set[str] = set()
    pattern = re.compile(r'componentName="([a-z][a-z0-9_]*)"')
    for path in CLIENT_SRC.rglob("*.tsx"):
        text = path.read_text()
        for match in pattern.finditer(text):
            refs.add(match.group(1))
    return refs


def test_every_frontend_component_help_ref_exists_in_json(topics_en):
    """Every componentName referenced in *.tsx must map to a component topic."""
    component_keys = {
        t["topic_key"] for t in topics_en if t["topic_type"] == "component"
    }
    refs = _collect_panel_help_button_refs()
    missing = refs - component_keys
    assert not missing, (
        f"PanelHelpButton references topic_keys not in help_topics.en.json: "
        f"{sorted(missing)}. Either add them to the JSON or fix the reference."
    )


def test_at_least_one_panel_help_ref_exists():
    """Sanity check: the scanner should find at least a few references."""
    refs = _collect_panel_help_button_refs()
    assert len(refs) >= 5, (
        f"Expected at least 5 PanelHelpButton references, found {len(refs)}. "
        f"Scanner may be broken."
    )


