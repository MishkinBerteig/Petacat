def test_typescript_constants_match_json(topics_en):
    """The generated TS constants must stay in sync with the JSON source."""
    assert TS_CONSTANTS.exists(), (
        f"{TS_CONSTANTS.relative_to(REPO_ROOT)} is missing. "
        f"Run: python scripts/generate_help_docs.py"
    )
    ts_text = TS_CONSTANTS.read_text()

    component_keys = sorted(
        t["topic_key"] for t in topics_en if t["topic_type"] == "component"
    )
    glossary_keys = sorted(
        t["topic_key"] for t in topics_en if t["topic_type"] == "glossary"
    )

    for key in component_keys:
        assert f"'{key}'" in ts_text, (
            f"component key '{key}' is in the JSON but missing from "
            f"{TS_CONSTANTS.name}. Run: python scripts/generate_help_docs.py"
        )
    for key in glossary_keys:
        assert f"'{key}'" in ts_text, (
            f"glossary key '{key}' is in the JSON but missing from "
            f"{TS_CONSTANTS.name}. Run: python scripts/generate_help_docs.py"
        )


def test_help_md_exists():
    """The generated HELP.md must exist for docs/CI to reference."""
    help_md = REPO_ROOT / "HELP.md"
    assert help_md.exists(), (
        "HELP.md is missing. Run: python scripts/generate_help_docs.py"
    )


