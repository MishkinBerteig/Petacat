# Admin action topics (structured bullet lists in metadata)
# ---------------------------------------------------------------------------

def test_all_required_admin_actions_present(topics_en):
    """Every Admin panel button must have a corresponding help topic."""
    keys = {t["topic_key"] for t in topics_en}
    missing = REQUIRED_ADMIN_ACTION_KEYS - keys
    assert not missing, (
        f"Required admin action help topics are missing: {sorted(missing)}"
    )


def test_admin_action_topics_are_component_type(topics_en):
    """Admin actions reuse topic_type='component' (served by the same API)."""
    by_key = {t["topic_key"]: t for t in topics_en}
    for key in REQUIRED_ADMIN_ACTION_KEYS:
        assert by_key[key]["topic_type"] == "component", (
            f"{key} must be topic_type=component so the /api/docs/components/"
            f"{{key}} endpoint serves it"
        )


def test_admin_action_topics_have_structured_metadata(topics_en):
    """Admin action topics must carry structured bullet lists in metadata.

    The AdminPanel renders these inline rather than as a single full_desc
    blob, so the schema requires:
      - metadata.kind == "admin_action"
      - metadata.button_variant in {warning, danger, utility}
      - metadata.user_description is a non-empty list of strings
      - metadata.technical_description is a non-empty list of strings
    """
    by_key = {t["topic_key"]: t for t in topics_en}
    for key in REQUIRED_ADMIN_ACTION_KEYS:
        topic = by_key[key]
        meta = topic.get("metadata") or {}

        assert meta.get("kind") == "admin_action", (
            f"{key}: metadata.kind must be 'admin_action'"
        )

        variant = meta.get("button_variant")
        assert variant in VALID_BUTTON_VARIANTS, (
            f"{key}: metadata.button_variant must be one of "
            f"{sorted(VALID_BUTTON_VARIANTS)}, got {variant!r}"
        )

        user_desc = meta.get("user_description")
        assert isinstance(user_desc, list) and len(user_desc) > 0, (
            f"{key}: metadata.user_description must be a non-empty list"
        )
        for i, bullet in enumerate(user_desc):
            assert isinstance(bullet, str) and bullet.strip(), (
                f"{key}: user_description[{i}] must be a non-empty string"
            )

        tech_desc = meta.get("technical_description")
        assert isinstance(tech_desc, list) and len(tech_desc) > 0, (
            f"{key}: metadata.technical_description must be a non-empty list"
        )
        for i, bullet in enumerate(tech_desc):
            assert isinstance(bullet, str) and bullet.strip(), (
                f"{key}: technical_description[{i}] must be a non-empty string"
            )


def test_admin_action_topics_have_empty_full_desc(topics_en):
    """Admin actions use metadata for structured content; full_desc stays empty.

    This guards against maintainers accidentally duplicating the description
    into both places. If someone wants to add a narrative summary, they should
    use short_desc (which is also shown by the /api/docs/components endpoint).
    """
    by_key = {t["topic_key"]: t for t in topics_en}
    for key in REQUIRED_ADMIN_ACTION_KEYS:
        assert by_key[key].get("full_desc", "") == "", (
            f"{key}: full_desc should be empty for admin actions; use "
            f"metadata.user_description and metadata.technical_description"
        )
